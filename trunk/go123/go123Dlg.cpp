// Cgo123Dlg 消息处理程序

BOOL Cgo123Dlg::OnInitDialog()
{
	CDialog::OnInitDialog();

	// 设置此对话框的图标。当应用程序主窗口不是对话框时，框架将自动
	//  执行此操作
	SetIcon(m_hIcon, TRUE);			// 设置大图标
	SetIcon(m_hIcon, FALSE);		// 设置小图标

	// TODO: 在此添加额外的初始化代码
	m_leftView.InsertColumn(0,"本地帐号",LVCFMT_LEFT,20);
	m_leftView.InsertColumn(1,"远程帐号",LVCFMT_LEFT,20);
	m_leftView.InsertColumn(2,"本地昵称",LVCFMT_LEFT,20);
	m_leftView.InsertColumn(3,"远程昵称",LVCFMT_LEFT,20);

	DWORD dwStyle;
	dwStyle = m_leftView.GetExtendedStyle();
	dwStyle |= LVS_EX_GRIDLINES|LVS_EX_FULLROWSELECT |LVS_EX_HEADERDRAGDROP ;
	m_leftView.SetExtendedStyle(dwStyle);	
	m_leftView.SetBkColor(RGB(0xFF, 0xFF, 0xFF));
	m_leftView.SetEditable(TRUE);

	return TRUE;  // 除非设置了控件的焦点，否则返回 TRUE
}

// 如果向对话框添加最小化按钮，则需要下面的代码
//  来绘制该图标。对于使用文档/视图模型的 MFC 应用程序，
//  这将由框架自动完成。

void Cgo123Dlg::OnPaint() 
{
	if (IsIconic())
	{
		CPaintDC dc(this); // 用于绘制的设备上下文

		SendMessage(WM_ICONERASEBKGND, reinterpret_cast<WPARAM>(dc.GetSafeHdc()), 0);

		// 使图标在工作矩形中居中
		int cxIcon = GetSystemMetrics(SM_CXICON);
		int cyIcon = GetSystemMetrics(SM_CYICON);
		CRect rect;
		GetClientRect(&rect);
		int x = (rect.Width() - cxIcon + 1) / 2;
		int y = (rect.Height() - cyIcon + 1) / 2;

		// 绘制图标
		dc.DrawIcon(x, y, m_hIcon);
	}
	else
	{
		CDialog::OnPaint();
	}
}

//当用户拖动最小化窗口时系统调用此函数取得光标显示。
HCURSOR Cgo123Dlg::OnQueryDragIcon()
{
	return static_cast<HCURSOR>(m_hIcon);
}

